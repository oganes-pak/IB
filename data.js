function formatCurrency(value) {
    return new Intl.NumberFormat('ru-RU', { style: 'currency', currency: 'RUB' }).format(value);
}

function getThreatFrequencyByPercentage(value) {
    if (value <= 25) return { level: "Очень Редко", coefficient: 0.1 };
    if (value <= 30) return { level: "Редко", coefficient: 0.35 };
    if (value <= 50) return { level: "Иногда", coefficient: 1.0 };
    if (value <= 75) return { level: "Часто", coefficient: 4.0 };
    return { level: "Очень Часто", coefficient: 12.0 };
}

function getVulnerabilityLevelByPercentage(value) {
    if (value <= 25) return { level: "Низкий", coefficient: 0.1 };
    if (value <= 30) return { level: "Средний", coefficient: 0.35 };
    if (value <= 55) return { level: "Высокий", coefficient: 0.5 };
    if (value <= 75) return { level: "Очень Высокий", coefficient: 0.8 };
    return { level: "Критический", coefficient: 1.0 };
}